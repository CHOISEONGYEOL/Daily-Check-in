/**
 * 채팅 욕설 필터 — 욕설/패드립 감지 시 메시지 차단 (조용히 무시)
 * 공백·숫자·특수문자 삽입 우회 방지
 */

// 한글+영문만 남기고 제거 (우회 방지)
const _strip = t => t.replace(/[^가-힣ㄱ-ㅎㅏ-ㅣa-zA-Z]/g, '');

// ── 금지 패턴 (정규식) ──
const _P = [
    // 씨발/시발 계열
    /[씨시쉬슈쒸씌][발빨벌펄팔]/,
    /씹/,
    // 새끼
    /[새색섹][끼키꺄]/,
    // 개+욕
    /개[새색년놈잡빡]/,
    // 병신
    /[병븅빙][신싄]/,
    // 좆
    /[좆좃졷]/,
    // 존나/졸라
    /[존졸좐][나라]/,
    // 지랄
    /지[랄럴]/,
    // 패드립
    /느금마/,
    /[니네]미/,
    /[니네]애미/,
    /[니네]어미/,
    /엠창/,
    // 성적 표현
    /보[지짓]/,
    /자[지짓]/,
    /섹[스쓰]/,
    // 기타 욕설
    /찐따/,
    /또라이/,
    /등[신싄]/,
    /걸[레래]/,
    /창[녀년]/,
    /미친[놈년새]/,
    /염[병뵹]/,
    /엿[먹머]/,
    /뒤[져저질]/,
    /꺼[져저]/,
    /닥[쳐처]/,
    /[꼴][통]/,
    /한[남녀]충/,
    // 영어 욕설
    /fuck/i,
    /shit/i,
    /bitch/i,
    /damn/i,
    /ass[h]/i,
];

// ── 자음 약어 ──
const _A = /ㅅㅂ|ㅆㅂ|ㅂㅅ|ㅅㄲ|ㅈㄹ|ㅂㅈ|ㅁㅊ|ㅄ|ㅗㅗ/;

/** 메시지가 깨끗하면 true, 욕설 포함이면 false */
export function isClean(text) {
    if (!text) return true;
    const s = _strip(text);
    for (const re of _P) {
        if (re.test(s)) return false;
    }
    if (_A.test(text)) return false;
    return true;
}
