// ── 자음 약어 ──
const _A = /ㅅㅂ|ㅆㅂ|ㅂㅅ|ㅅㄲ|ㅈㄹ|ㅂㅈ|ㅁㅊ|ㅄ|ㅗ+|ㄲㅈ|ㅍㅌㅊ|ㄴㄱㅁ|ㅆㄹㄱ|ㄷㅊ/;

/** 메시지가 깨끗하면 true, 욕설 포함이면 false */
export function isClean(text) {
    if (!text) return true;
    const s = _strip(text);
    for (const re of _P) {
        if (re.test(s)) return false;
    }
    if (_A.test(text)) return false;
    return true;
}

// ══════════════════════════════════════════════
//  닉네임 전용 필터 (욕설 + 실명/정치인 차단)
